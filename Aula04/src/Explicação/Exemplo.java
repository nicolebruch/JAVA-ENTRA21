package Explicação;

public class Exemplo {

	public static void main(String[] args) {

		// Repita - valor de início - valor de fim - valor que acrescenta
		// Fazer algoritmo que printe do número até o número 10.

		for (int n = 20; n > 9; n--) { // n++ == n = n+1 ou n-- == n == n-1
			System.out.println(n);
		}

	}

}